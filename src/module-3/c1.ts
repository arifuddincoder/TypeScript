class Animal {
	name: string;
	species: string;
	sound: string;

	constructor(name: string, species: string, sound: string) {
		this.name = name;
		this.species = species;
		this.sound = sound;
	}

	makeSound() {
		return `${this.name} is making ${this.sound}`;
	}
}

const kutta = new Animal("German Sphered", "Dog", "ghew ghew");

console.log(kutta.makeSound());
